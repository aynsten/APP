	import uniNumberBox from '@/components/uni-number-box.vue'
	import share from '@/components/share';
	import {
		api,
		requestsuccess
	} from '../../api/request.js'
	export default {
		components: {
			share,
			uniNumberBox
		},
		data() {
			return {
				specClass: 'none',
				specSelected: [],
				favorite: false,
				shareList: [{
						type: 1,
						icon: '/static/temp/share_wechat.png',
						text: '微信好友'
					},
					{
						type: 2,
						icon: '/static/temp/share_moment.png',
						text: '朋友圈'
					},
					{
						type: 3,
						icon: '/static/temp/share_qq.png',
						text: 'QQ好友'
					},
					{
						type: 4,
						icon: '/static/temp/share_qqzone.png',
						text: 'QQ空间'
					}
				],
				specChildList: [],
				colorlist: [],
				styleID: '',
				datagroup: {},
				userInfo: {},
				hasLogin: false,
				pType: 0, //0 商家 1 分销商  2 省代 3 市代 4 区代 
				num: 1,
				sendorderlist: [], //立即购买商品缓存
				orderPrice: 0,
				cartList: [], //购物车商品缓存
				tempstore: 0,
				sizeactive: 0,
				coloractive: -1,
				sizegroup: {},
				colorgroup: {},
			};
		},
		async onLoad(options) {
			this.styleID = options.styleID
		},
		onShow() {
			let that = this
			this.loaddata()
			this.WhetherCollection()
			uni.removeStorage({
				key: 'sendorderlist',
			});
		},
		methods: {
			loaddata() {
				let that = this
				// 获取缓存登录信息
				uni.getStorage({
					key: 'userInfo',
					success: function(res) {
						that.userInfo = res.data
						that.pLevel = res.data.customer.pLevel
					},
					fail(res) {
						if (res.data == '') {}
					}
				})

				// 获取登录状态
				uni.getStorage({
					key: "hasLogin",
					success: function(res) {
						that.hasLogin = res.data
					},
					fail(res) {
						if (res.data == '') {
							that.hasLogin = false
						}
					}
				})
				// 请求数据
				uni.request({
					url: api + "/api/NewStyle/GetStyleDetail?style=" + that.styleID,
					success(res) {
						if (res.data.statusCode == requestsuccess) {
							that.datagroup = res.data.data
							let styleSkus = res.data.data.styleSkus //尺码
							let template = []
							that.colorlist = []
							that.sizegroup = res.data.data.styleSkus[0]
							styleSkus.forEach((item, index) => {
								item.store = index
								item.colors.forEach((v, k) => {
									v.store = index
									that.colorlist.push(v)
								})
							})
						}
					}
				})
			},
			//分享
			share() {
				this.$refs.share.toggleMask();
			},
			// 获取收藏状态
			WhetherCollection() {
				let that = this
				uni.request({
					url: api + "/api/CustomerCollect/IsExist?style=" + that.styleID,
					header: {
						"Authorization": this.userInfo.token_type + ' ' + this.userInfo.access_token
					},
					success(res) {
						if (res.data.statusCode == requestsuccess) {
							that.favorite = res.data.data
						}
					}
				})
			},
			//规格弹窗开关
			toggleSpec() {
				if (this.specClass === 'show') {
					this.specClass = 'hide';
					setTimeout(() => {
						this.specClass = 'none';
					}, 250);
				} else if (this.specClass === 'none') {
					this.specClass = 'show';
				}
			},
			//选择尺寸
			sizechecked(item, index) {
				this.tempstore = item.store
				this.sizeactive = index
				this.sizegroup = item
			},
			//选择颜色
			colorchecked(item, index) {
				this.coloractive = index
				this.colorgroup = item
			},
			//收藏
			toFavorite() {
				let that = this
				if (that.favorite == false) {
					uni.request({
						url: api + "/api/CustomerCollect/InsertCustomerCollect?style=" + that.styleID,
						method: "POST",
						header: {
							"Authorization": that.userInfo.token_type + ' ' + that.userInfo.access_token
						},
						success(res) {
							if (res.data.statusCode == requestsuccess) {
								that.favorite = true;
								that.WhetherCollection()
								that.$api.msg('收藏成功！');
							}
						}
					})
				} else {
					uni.request({
						url: api + "/api/CustomerCollect/DeleteCustomerCollect?style=" + that.styleID,
						method: "POST",
						header: {
							"Authorization": that.userInfo.token_type + ' ' + that.userInfo.access_token
						},
						success(res) {
							if (res.data.statusCode == requestsuccess) {
								that.favorite = false
								that.WhetherCollection()
								that.$api.msg('取消收藏成功！');
							}
						}
					})
				}


			},
			// 数量
			numberChange(e) {
				this.num = e.number
			},
			// 立即购买
			buy() {
				let that = this
				if (Object.keys(that.colorgroup).length == 0) {
					uni.showModal({
						content: '请选择规格',
						showCancel: false,
						success: function(res) {
							if (res.confirm) {
								that.specClass = 'show';
							}
						}
					});
					return
				}
				if (!this.hasLogin) {
					uni.showModal({
						content: '请登录后再进行操作!',
						showCancel: false,
						success: function(res) {
							if (res.confirm) {
								uni.navigateTo({
									url: "../public/login"
								})
							}
						}
					});
					return
				}
				// 获取登录状态
				uni.getStorage({
					key: "hasLogin",
					success: function(res) {
						that.hasLogin = res.data
						console.log(res.data)
					},
					fail(res) {
						if (res.data == '') {
							that.hasLogin = false
						}
					}
				})
				that.datagroup.styleID = that.styleID
				that.datagroup.orderQty = that.num
				that.datagroup.orderPrice = that.datagroup.price
				that.datagroup.sizegroup = that.sizegroup
				that.datagroup.colorgroup = that.colorgroup
				that.datagroup.checked = true
				that.sendorderlist[0] = that.datagroup
				console.log(that.sendorderlist)
				uni.setStorage({
					key: 'sendorderlist',
					data: that.sendorderlist,
				});
				uni.navigateTo({
					url: "../order/sendorder"
				})
			},
			// 加入购物车
			addcart() {
				let that = this
				uni.getStorage({
					key: 'cartList',
					success: function(res) {
						that.cartList = res.data
					}
				});
				if (Object.keys(that.colorgroup).length == 0) {
					uni.showModal({
						content: '请选择规格',
						showCancel: false,
						success: function(res) {
							if (res.confirm) {
								that.specClass = 'show';
							}
						}
					});
					return
				}
				that.orderPrice = that.datagroup.price
				that.datagroup.styleID = that.styleID
				that.datagroup.orderQty = that.num
				that.datagroup.orderPrice = that.orderPrice
				that.datagroup.sizegroup = that.sizegroup
				that.datagroup.colorgroup = that.colorgroup
				that.datagroup.checked = true
				let count = 0
				if (that.cartList.length != 0) {
					that.cartList.filter(item => item.styleID == that.datagroup.styleID && item.sizegroup.size == that.sizegroup.size &&
						item.colorgroup.label == that.colorgroup.label).forEach(i => {
						count = 1
						i.orderQty = i.orderQty + 1
						this.$api.msg('宝贝已成功添加购物车!');
					})
				}
				if (count == 0) {
					that.cartList.push(that.datagroup)
					this.$api.msg('宝贝已成功添加购物车!');
				}
				uni.setStorage({
					key: 'cartList',
					data: that.cartList,
				});
			},
			stopPrevent() {}
		},

	}
</script>

